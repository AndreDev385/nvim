  before_init = function(_, config)
    if not config.settings then
      config.settings = {}
    end
    if not config.settings.editor then
      config.settings.editor = {}
    end
    if not config.settings.editor.tabSize then
      config.settings.editor.tabSize = vim.o.shiftwidth
    end
  end,
  workspace_required = true,
  root_dir = function(bufnr)
    local fname = vim.api.nvim_buf_get_name(bufnr)
    local root_files = {
      'tailwind.config.js',
      'tailwind.config.cjs',
      'tailwind.config.mjs',
      'tailwind.config.ts',
      'postcss.config.js',
      'postcss.config.cjs',
      'postcss.config.mjs',
      'postcss.config.ts',
      'theme/static_src/tailwind.config.js',
      'theme/static_src/tailwind.config.cjs',
      'theme/static_src/tailwind.config.mjs',
      'theme/static_src/tailwind.config.ts',
      'theme/static_src/postcss.config.js',
      '.git',
    }
    local match = vim.fs.find(root_files, { path = fname, upward = true })[1]
    if match then
      return vim.fs.dirname(match)
    end
    local pkg = vim.fs.find('package.json', { path = fname, upward = true })[1]
    if pkg then
      local ok, content = pcall(vim.fn.readfile, pkg)
      if ok then
        local json = vim.json.decode(table.concat(content, '\n'))
        if json and (json.devDependencies and json.devDependencies.tailwindcss or json.dependencies and json.dependencies.tailwindcss) then
          return vim.fs.dirname(pkg)
        end
      end
    end
    return nil
  end,
}
